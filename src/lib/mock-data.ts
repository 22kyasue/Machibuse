export function getMansionById(id: string): MansionWithStats | undefined {
  return mockMansions.find((m) => m.id === id);
}

export function getUnitsByMansionId(mansionId: string): UnitWithStats[] {
  return mockUnits[mansionId] || [];
}

export function getUnitById(unitId: string): UnitWithStats | undefined {
  for (const units of Object.values(mockUnits)) {
    const unit = units.find((u) => u.id === unitId);
    if (unit) return unit;
  }
  return undefined;
}

export function getListingsByUnitId(unitId: string): Listing[] {
  return mockListings[unitId] || [];
}

export function getListingById(listingId: string): Listing | undefined {
  for (const listings of Object.values(mockListings)) {
    const listing = listings.find((l) => l.id === listingId);
    if (listing) return listing;
  }
  return undefined;
}
